import UIKit

extension UIColor {
    class var pillBlue: UIColor {
        return UIColor(red: 0, green: 0.604, blue: 0.831, alpha: 1)
    }
    
    class var pillBlueLowAlpha: UIColor {
        return UIColor(red: 0, green: 0.604, blue: 0.831, alpha: 0.3)
    }
    
    class var pillGreen: UIColor {
        return UIColor(red: 0.478, green: 0.667, blue: 0.212, alpha: 1)
    }
    
    class var pillGreenLowAlpha: UIColor {
        return UIColor(red: 0.478, green: 0.667, blue: 0.212, alpha: 0.3)
    }
    
    class var pillRed: UIColor {
        return UIColor(red: 0.9, green: 0.2, blue: 0.2, alpha: 1)
    }
    
    class var pillRedLowAlpha: UIColor {
        return UIColor(red: 0.9, green: 0.2, blue: 0.2, alpha: 0.3)
    }
}
